#include "libft.h"

void	ft_striter(char *str, void (*f)(char *))
{
	int i;

	i = 0;
	if (str != '\0' && f != '\0')
	{
		while (str[i] != '\0')
		{
			f(str + i);
			i++;
		}
	}
}
